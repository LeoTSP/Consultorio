    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        int opcion = JOptionPane.showConfirmDialog(null, "Â¿Realmente desea insertar el registro?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (opcion == JOptionPane.YES_OPTION) {
            boolean botones = false;
            String nombre = txtNombre.getText();
            String edad = txtEdad.getText();
            String sexo = txtSexo.getText();
            String especialidad = txtEspecialidad.getText();

            if (nombre.isEmpty() || edad.isEmpty() || sexo.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Verifique que todos los datos esten llenados correctamente!");
            } else {
                instruccionesDentista objDentista= new instruccionesDentista();
                objDentista.InsertarDentista(nombre, edad, sexo, especialidad);
                txtNombre.setText("");
                txtEdad.setText("");
                txtSexo.setText("");
                txtEspecialidad.setText("");

            }
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

