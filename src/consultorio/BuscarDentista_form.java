    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            String tBuscar = txtBuscar.getText();
            instruccionesDentista objClientes= new instruccionesDentista();
            misRegistros= objClientes.BuscarDentista(tBuscar);
            misRegistros.next();
            txtNombre.setText(misRegistros.getString(2));
            txtEdad.setText(misRegistros.getString(3));
            txtSexo.setText(misRegistros.getString(4));
            txtEspecialidad.setText(misRegistros.getString(5));
        }catch(Exception er){
            JOptionPane.showMessageDialog(null, "Registro inexistente, Verifique de nuevo" , "Error", JOptionPane.ERROR_MESSAGE);
            txtBuscar.setText("");
            txtNombre.setText("");
            txtEdad.setText("");
            txtSexo.setText("");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

