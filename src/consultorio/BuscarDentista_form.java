    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(null, "¿Realmente desea insertar el registro?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (opcion == JOptionPane.YES_OPTION) {
            boolean botones = false;
            String Buscar = txtBuscar.getText();
            String nombre = txtNombre.getText();
            String edad = txtEdad.getText();
            String sexo = txtSexo.getText();
            String especialidad = txtEspecialidad.getText();

            if (nombre.isEmpty() || edad.isEmpty() || sexo.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Verifique que todos los datos esten llenados correctamente!");
            } else {
                instruccionesDentista objDentista = new instruccionesDentista();
                objDentista.EditarDentista(nombre, edad, sexo, especialidad, Buscar);
                txtEspecialidad.setText("");
                txtNombre.setText("");
                txtEdad.setText("");
                txtSexo.setText("");
                txtBuscar.setText("");
                
                txtNombre.setEditable(false);
                txtEdad.setEditable(false);
                txtSexo.setEditable(false);
                txtEspecialidad.setEditable(false);
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

